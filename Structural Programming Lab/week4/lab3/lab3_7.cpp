#include <iostream>
using namespace std;
int main()
{
    int n = 13;
    cout << n << endl;
    cout << "n >> 1 = " << (n >> 2) << endl;
    cout << "n << 3 = " << (n << 3) << endl;
    cout << "n & 3 = " << (n & 3) << endl;
    cout << "n | 3 = " << (n | 3) << endl;
    cout << "n ^ 3 = " << (n ^ 3) << endl ;
    return 0;
}